/**
 * Round-trips for the higher-shape setters now generated by the AST
 * mutation pipeline: {@code @Negate} inverse-boolean pair,
 * {@code @Formattable} {@code @PrintFormat} overload, and
 * {@code @Collector} bulk + opt-in add/put/clear/compute families.
 */
public class AdvancedSetterShapesTest {

    private static Compilation compile(JavaFileObject... sources) {
        return Compiler.javac().withProcessors(new ClassBuilderProcessor()).compile(sources);
    }

    private static ClassLoader loadClasses(Compilation compilation) throws Exception {
        Path tmp = Files.createTempDirectory("classbuilder-shapes-test");
        for (JavaFileObject f : compilation.generatedFiles()) {
            if (f.getKind() != JavaFileObject.Kind.CLASS) continue;
            String uri = f.toUri().toString();
            int anchor = uri.indexOf("CLASS_OUTPUT/");
            String rel = anchor >= 0 ? uri.substring(anchor + "CLASS_OUTPUT/".length()) : f.getName();
            Path out = tmp.resolve(rel);
            Files.createDirectories(out.getParent());
            try (InputStream in = f.openInputStream()) {
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                in.transferTo(baos);
                Files.write(out, baos.toByteArray());
            }
        }
        return new URLClassLoader(new URL[]{tmp.toUri().toURL()}, AdvancedSetterShapesTest.class.getClassLoader());
    }

    private static Class<?> nested(Class<?> outer, String simpleName) {
        for (Class<?> inner : outer.getDeclaredClasses()) {
            if (inner.getSimpleName().equals(simpleName)) return inner;
        }
        fail("expected nested '" + simpleName + "' on " + outer);
        return null;
    }

    // ------------------------------------------------------------------
    // @Negate
    // ------------------------------------------------------------------

    @Test
    public void negateAnnotation_emitsInversePair() throws Exception {
        JavaFileObject src = JavaFileObjects.forSourceLines("demo.Toggle",
            "package demo;",
            "import dev.simplified.annotations.ClassBuilder;",
            "import dev.simplified.annotations.Negate;",
            "@ClassBuilder(validate = false)",
            "public class Toggle {",
            "    @Negate(\"enabled\") boolean disabled;",
            "    public Toggle(boolean disabled) { this.disabled = disabled; }",
            "    public boolean isDisabled() { return disabled; }",
            "}");
        Compilation c = compile(src);
        assertThat(c).succeeded();

        ClassLoader cl = loadClasses(c);
        Class<?> toggle = Class.forName("demo.Toggle", true, cl);
        Class<?> builder = nested(toggle, "Builder");

        // Direct setter sets disabled.
        Object b1 = toggle.getMethod("builder").invoke(null);
        builder.getMethod("isDisabled").invoke(b1);
        Object t1 = builder.getMethod("build").invoke(b1);
        assertEquals(Boolean.TRUE, toggle.getMethod("isDisabled").invoke(t1));

        // Inverse setter unsets disabled (sets enabled = true → disabled = false).
        Object b2 = toggle.getMethod("builder").invoke(null);
        builder.getMethod("isEnabled").invoke(b2);
        Object t2 = builder.getMethod("build").invoke(b2);
        assertEquals(Boolean.FALSE, toggle.getMethod("isDisabled").invoke(t2));

        // Typed inverse: isEnabled(true) -> disabled=false; isEnabled(false) -> disabled=true.
        Object b3 = toggle.getMethod("builder").invoke(null);
        builder.getMethod("isEnabled", boolean.class).invoke(b3, true);
        assertEquals(Boolean.FALSE, toggle.getMethod("isDisabled").invoke(builder.getMethod("build").invoke(b3)));
        Object b4 = toggle.getMethod("builder").invoke(null);
        builder.getMethod("isEnabled", boolean.class).invoke(b4, false);
        assertEquals(Boolean.TRUE, toggle.getMethod("isDisabled").invoke(builder.getMethod("build").invoke(b4)));
    }

    // ------------------------------------------------------------------
    // @Formattable on plain String
    // ------------------------------------------------------------------

    @Test
    public void formattableString_overloadFormatsArgs() throws Exception {
        JavaFileObject src = JavaFileObjects.forSourceLines("demo.Msg",
            "package demo;",
            "import dev.simplified.annotations.ClassBuilder;",
            "import dev.simplified.annotations.Formattable;",
            "@ClassBuilder(validate = false)",
            "public class Msg {",
            "    @Formattable String text;",
            "    public Msg(String text) { this.text = text; }",
            "    public String getText() { return text; }",
            "}");
        Compilation c = compile(src);
        assertThat(c).succeeded();

        ClassLoader cl = loadClasses(c);
        Class<?> msg = Class.forName("demo.Msg", true, cl);
        Class<?> builder = nested(msg, "Builder");

        Object b = msg.getMethod("builder").invoke(null);
        // Plain setter unchanged
        builder.getMethod("text", String.class).invoke(b, "hello");
        Object plain = builder.getMethod("build").invoke(b);
        assertEquals("hello", msg.getMethod("getText").invoke(plain));

        // @PrintFormat overload formats with args. Method.invoke special-cases
        // a trailing Object[] arg matching a varargs parameter: the array is
        // forwarded as the varargs payload directly, no extra wrapping.
        Object b2 = msg.getMethod("builder").invoke(null);
        Method formatted = builder.getMethod("text", String.class, Object[].class);
        formatted.invoke(b2, "hi %s, %d", new Object[]{"world", 7});
        Object out = builder.getMethod("build").invoke(b2);
        assertEquals("hi world, 7", msg.getMethod("getText").invoke(out));
    }

    // ------------------------------------------------------------------
    // @Formattable on Optional<String>
    // ------------------------------------------------------------------

    @Test
    public void formattableOptional_storesFormattedOptional() throws Exception {
        JavaFileObject src = JavaFileObjects.forSourceLines("demo.OptMsg",
            "package demo;",
            "import dev.simplified.annotations.ClassBuilder;",
            "import dev.simplified.annotations.Formattable;",
            "import java.util.Optional;",
            "@ClassBuilder(validate = false)",
            "public class OptMsg {",
            "    @Formattable Optional<String> description;",
            "    public OptMsg(Optional<String> description) { this.description = description; }",
            "    public Optional<String> getDescription() { return description; }",
            "}");
        Compilation c = compile(src);
        assertThat(c).succeeded();

        ClassLoader cl = loadClasses(c);
        Class<?> opt = Class.forName("demo.OptMsg", true, cl);
        Class<?> builder = nested(opt, "Builder");

        Object b = opt.getMethod("builder").invoke(null);
        Method formattedSetter = builder.getMethod("description", String.class, Object[].class);
        formattedSetter.invoke(b, "p=%s", new Object[]{"X"});
        Object built = builder.getMethod("build").invoke(b);
        Optional<?> got = (Optional<?>) opt.getMethod("getDescription").invoke(built);
        assertTrue(got.isPresent());
        assertEquals("p=X", got.get());

        // Null format arg yields Optional.empty()
        Object b2 = opt.getMethod("builder").invoke(null);
        formattedSetter.invoke(b2, null, new Object[0]);
        Optional<?> got2 = (Optional<?>) opt.getMethod("getDescription").invoke(builder.getMethod("build").invoke(b2));
        assertTrue("null format string must yield Optional.empty", got2.isEmpty());
    }

    // ------------------------------------------------------------------
    // @Collector on List
    // ------------------------------------------------------------------

    @Test
    public void collectorList_replaceAddClear() throws Exception {
        JavaFileObject src = JavaFileObjects.forSourceLines("demo.Bag",
            "package demo;",
            "import dev.simplified.annotations.ClassBuilder;",
            "import dev.simplified.annotations.Collector;",
            "import java.util.List;",
            "@ClassBuilder(validate = false)",
            "public class Bag {",
            "    @Collector(singular = true, clearable = true) List<String> tags;",
            "    public Bag(List<String> tags) { this.tags = tags; }",
            "    public List<String> getTags() { return tags; }",
            "}");
        Compilation c = compile(src);
        assertThat(c).succeeded();

        ClassLoader cl = loadClasses(c);
        Class<?> bag = Class.forName("demo.Bag", true, cl);
        Class<?> builder = nested(bag, "Builder");

        // varargs replace
        Object b = bag.getMethod("builder").invoke(null);
        builder.getMethod("tags", String[].class).invoke(b, (Object) new String[]{"a", "b"});
        Object built = builder.getMethod("build").invoke(b);
        assertEquals(List.of("a", "b"), bag.getMethod("getTags").invoke(built));

        // iterable replace
        Object b2 = bag.getMethod("builder").invoke(null);
        builder.getMethod("tags", Iterable.class).invoke(b2, List.of("x", "y", "z"));
        assertEquals(List.of("x", "y", "z"), bag.getMethod("getTags").invoke(builder.getMethod("build").invoke(b2)));

        // singular add (default prefix is empty so name is addTag)
        Object b3 = bag.getMethod("builder").invoke(null);
        builder.getMethod("tags", String[].class).invoke(b3, (Object) new String[]{"first"});
        builder.getMethod("addTag", String.class).invoke(b3, "second");
        assertEquals(List.of("first", "second"), bag.getMethod("getTags").invoke(builder.getMethod("build").invoke(b3)));

        // clear
        Object b4 = bag.getMethod("builder").invoke(null);
        builder.getMethod("tags", String[].class).invoke(b4, (Object) new String[]{"to-be-cleared"});
        builder.getMethod("clearTags").invoke(b4);
        assertTrue(((List<?>) bag.getMethod("getTags").invoke(builder.getMethod("build").invoke(b4))).isEmpty());
    }

    // ------------------------------------------------------------------
    // @Collector on Map
    // ------------------------------------------------------------------

    @Test
    public void collectorMap_replacePutClear() throws Exception {
        JavaFileObject src = JavaFileObjects.forSourceLines("demo.Dict",
            "package demo;",
            "import dev.simplified.annotations.ClassBuilder;",
            "import dev.simplified.annotations.Collector;",
            "import java.util.Map;",
            "@ClassBuilder(validate = false)",
            "public class Dict {",
            "    @Collector(singularMethodName = \"entry\", singular = true, clearable = true) Map<String, Integer> entries;",
            "    public Dict(Map<String, Integer> entries) { this.entries = entries; }",
            "    public Map<String, Integer> getEntries() { return entries; }",
            "}");
        Compilation c = compile(src);
        assertThat(c).succeeded();

        ClassLoader cl = loadClasses(c);
        Class<?> dict = Class.forName("demo.Dict", true, cl);
        Class<?> builder = nested(dict, "Builder");

        // map replace
        Object b = dict.getMethod("builder").invoke(null);
        Map<String, Integer> seed = new LinkedHashMap<>();
        seed.put("a", 1);
        seed.put("b", 2);
        builder.getMethod("entries", Map.class).invoke(b, seed);
        Map<?, ?> result = (Map<?, ?>) dict.getMethod("getEntries").invoke(builder.getMethod("build").invoke(b));
        assertEquals(seed, result);

        // putEntry adds
        Object b2 = dict.getMethod("builder").invoke(null);
        builder.getMethod("putEntry", String.class, Integer.class).invoke(b2, "x", 9);
        builder.getMethod("putEntry", String.class, Integer.class).invoke(b2, "y", 10);
        Map<?, ?> result2 = (Map<?, ?>) dict.getMethod("getEntries").invoke(builder.getMethod("build").invoke(b2));
        assertEquals(2, result2.size());
        assertEquals(9, result2.get("x"));

        // clear empties
        Object b3 = dict.getMethod("builder").invoke(null);
        builder.getMethod("putEntry", String.class, Integer.class).invoke(b3, "k", 1);
        builder.getMethod("clearEntries").invoke(b3);
        assertTrue(((Map<?, ?>) dict.getMethod("getEntries").invoke(builder.getMethod("build").invoke(b3))).isEmpty());
    }

}
